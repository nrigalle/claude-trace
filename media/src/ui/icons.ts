const PALETTE = [
  "#3b82f6", "#8b5cf6", "#10b981", "#14b8a6",
  "#f59e0b", "#ec4899", "#6366f1", "#06b6d4",
  "#f97316", "#a855f7",
] as const;

export const getToolColor = (name: string): string => {
  const hit = TOOL_COLORS_BASE[name];
  if (hit) return hit;
  let h = 0;
  for (let i = 0; i < name.length; i++) {
    h = (h * 31 + name.charCodeAt(i)) | 0;
  }
  return PALETTE[Math.abs(h) % PALETTE.length] ?? PALETTE[0];
};

const iconTemplateCache = new Map<IconName, HTMLTemplateElement>();

export const icon = (name: IconName, size = 14): HTMLElement => {
  let template = iconTemplateCache.get(name);
  if (!template) {
    template = document.createElement("template");
    template.innerHTML = `<span class="icon-wrapper">${ICONS[name]}</span>`;
    iconTemplateCache.set(name, template);
  }
  const wrapper = template.content.firstChild!.cloneNode(true) as HTMLElement;
  wrapper.style.width = `${size}px`;
  wrapper.style.height = `${size}px`;
  wrapper.style.display = "inline-flex";
  const svgEl = wrapper.querySelector("svg");
  if (svgEl) {
    svgEl.setAttribute("width", String(size));
    svgEl.setAttribute("height", String(size));
  }
  return wrapper;
};
